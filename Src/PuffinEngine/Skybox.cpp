void Skybox::setTexture(TexturePtr texture) {
    if (!texture) {
        logError("Skybox::setTexture()", PUFFIN_MSG_NULL_OBJECT);
        return;
    }

    if (texture->getType() != TextureType::TextureCube) {
        logError("Skybox::setTexture()", PUFFIN_MSG_TEXTURE_INVALID_TYPE);
        return;
    }

    texture_ = texture;
}